public class Operators {

    public static void main(String[] args) {
        System.out.println("AND operator : " + (5 & 6));
        System.out.println("OR operator : " + (5 | 6));
        System.out.println("XOR operator : " + (5 ^ 6));
        System.out.println("1's complement operator : " + (~5));
        System.out.println("Left Shift operator : " + (5 << 2));
        System.out.println("Right Shift operator : " + (5 >> 2));
    }
}