public class Types{
    public static void main(String args[]){
        byte b = 127; //-127 to +127
        System.out.println(b);/* Printing value of b */
        Short s = 1000;
        System.out.println(s);
        char c = 'R';
        System.out.println(c);
        boolean bo = true;
        System.out.println(bo);
        int i = 12767;
        System.out.println(i);
        long l = 1298765456L;
        System.out.println(l);
        float f = 12.12345f;
        System.out.println(f);
        double d = 127.098765;
        System.out.println(d);

    }
}